public class LevelData {
    public int lvlId, e1, e2, e3, e4;
    public float spawnDelay;

    public LevelData(int lvlId, float spawnDelay, int e1, int e2, int e3, int e4) {
        this.lvlId = lvlId;
        this.spawnDelay = spawnDelay;
        this.e1 = e1;
        this.e2 = e2;
        this.e3 = e3;
        this.e4 = e4;

    }
}